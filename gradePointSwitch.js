
// find grade point by switch 
let marks = 70;

switch(marks){
    case 80:
        console.log("Your Grade is A+")
    break;
    case 70:
        console.log("Your Grade is A")
    break;
    case 60:
        console.log("Your Grade is A-")
    break;
    case 50:
        console.log("Your Grade is B")
    break;
    case 40:
        console.log("Your Grade is C")
    break;
    case 33:
        console.log("Your Grade is D")
    break;
    case 0:
        console.log("Your Grade is Fail")
    break;
}

